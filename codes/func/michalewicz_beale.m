%michaelwicz
%--------------------------------------------------------------------------
clc;
clear all;
%~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
t=10^(-7);
syms x y

michaelwicz(x,y)= - (sin(x) * (sin(1 * x^2/pi))^(2 * 10)) - (sin(y) * (sin(2 * y^2/pi))^(2 * 10))...
                  + t*( (1.5-x*(1-y))^2+(2.25-x*(1-y^2))^2+(2.625-x*(1-y^3))^2 );
pretty(michaelwicz(x,y))
%~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
figure(1)
ezsurf(michaelwicz(x,y),[-10 10],170)
